// Program Description:
//
// This program presents an example of use of the Timer0 of the C8051F99x in
// two 8-bit reload counter/timer mode. It uses the 'F99xDK as HW platform.
//
// It sets the two timers to interrupt every millisecond.
//
// When the timer low interrupts a counter (low_counter) increments and upon
// reaching the value defined by YELLOW_TOGGLE_RATE toggles the Yellow LED
// When the timer high interrupts a counter (high_counter) increments and upon
// reaching the value defined by RED_TOGGLE_RATE toggles the RED LED
//
// Pinout:
//
//    P1.3 -> Yellow LED (toggled by TL0)
//    P1.5 -> Red LED    (toggled by TH0)
//
//    all other port pins unused
//
// How To Test:
//
// 1) Ensure that jumpers are placed on the following:
//       J8:   P1.3/LED1.3
//       J11:  PWR/WALL_PWR
//       J17:  VDD_PIN/PWR
// 2) Connect the USB Debug Adapter to J4.
// 3) Turn the POWER switch (SW5) to the "ON" position.
// 4) If a different LED blink rates are desired change the following constant:
//       -> YELLOW_TOGGLE_RATE
//       -> RED_TOGGLE_RATE
// 5) Compile and download code to a 'F99x device on a C8051F99x-TB development
//    board by selecting Run -> Debug from the menus, clicking the Debug button
//    in the quick menu, or pressing F11.
// 6) Run the code by selecting Run -> Resume from the menus, clicking the
//    Resume button in the quick menu, or pressing F8.
// 7) The Yellow LED and Red LED should blink.
//
//
// Target:         C8051F99x-C8051F98x
// Tool chain:     Simplicity Studio / Keil C51 9.51
// Command Line:   None
//
// Release 1.1 (BL)
